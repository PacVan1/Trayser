template <typename T>
inline static constexpr std::string GetNullSeparated()
{
    // Generated by AI
    std::string result;
    constexpr auto names = magic_enum::enum_names<T>();
    for (std::size_t i = 0; i < names.size() - 1; i++) 
    {
        result += names[i];
        result += '\0';
    }
    result += '\0'; // Final null terminator
    return result;
}

